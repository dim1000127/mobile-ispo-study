package com.example.laba40;

public class Message {
    private long id;
    private long time;
    private String text;
    private String image;

    public long getId() {
        return id;
    }

    public long getTime() {
        return time;
    }

    public String getText() {
        return text;
    }

    public String getImage() {
        return image;
    }
}
